const { DataTypes } = require("sequelize");
const sequelize = require("../config/database");

const StudyGroup = sequelize.define("StudyGroup", {
    id: {
        type: DataTypes.INTEGER,
        primaryKey: true,
        autoIncrement: true,
    },
    name: {
        type: DataTypes.STRING,
        allowNull: false,
    },
    description: {
        type: DataTypes.TEXT,
        allowNull: true,
    },
});

module.exports = StudyGroup;
